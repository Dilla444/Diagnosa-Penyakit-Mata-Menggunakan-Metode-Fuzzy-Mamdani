                                  <h3>Informasi Kesehatan Mata</h3>
                                  <div>
                                    <p><img height="158" width="179" src="images/angelina jolie.jpg" style="float:left;" /><strong>Cara Menjaga Kesehatan Mata</strong>. Mata adalah organ yang sangat penting pada tubuh kita. Dengan mata, kita dapat melihat keindahan dunia ini. Oleh karena itu, penting sekali bagi kita untuk tetap <strong>menjaga kesehatan mata</strong>. Kita harus senantiasa menjaga kesehatan mata kita agar terhindar dari berbagai penyakit mata.<br />
                                      <br />
                                      Banyak sekali penyakit mata yang dapat menyerang mata kita sewaktu-waktu. Seperti seseorang yang sering membaca buku, bermain game terlalu lama, dan sebagainya dapat terserang penyakit miopi.<br />
                                      <br />
                                      Penyakit <a href="http://id.wikipedia.org/wiki/Mata" rel="nofollow" target="_blank">mata</a> yang sudah tak asing lagi kita dengar seperti miopi atau rabun jauh, hipermetropi atau rabun dekat, presbiopi atau tidak dapat melihat benda yang jauh maupun dekat, katarak, buta warna yang tidak dapat melihat berbagai macam warna. Ada banyak hal yang dapat Anda lakukan untuk menjaga <strong>kesehatan mata</strong> dan dapat terhindar dari penyakit mata tersebut. Berikut adalah tips cara menjaga kesehatan mata.<img height="158" width="179" src="images/2137_tear_film_label_v3_250.jpg" style="float:right;" /><br />
                                    </p>
                                    <h3>Cara Menjaga Kesehatan Mata</h3>
                                    <ol>
                                      <li>Riwayat kesehatan mata keluarga</li>
                                      Bicaralah pada anggota keluarga terutama orang tua Anda. Apakah orang tua Anda di diagnosis mengalami penyakit mata keturunan. Hal ini karena dapat menentukan Anda, apakah Anda beresiko tinggi terkena penyakit mata atau tidak.<br />
                                      <br />
                                      <li>Jaga jarak pandang</li>
                                      Jaga jarak pandang ketika sedang berada dilayar komputer maupun TV. Jarak pandang yang ideal sekitar 50-100cm. Seperti juga halnya ketika sedang membaca buku. Jarak yang ideal ketika membaca buku sekitar 30cm. Hal ini dimaksudkan agar dapat terhindar dari miopi atau rabun dekat.<br />
                                      <br />
                                      <li>Hindari kebiasaan mengucek mata</li>
                                      Jangan sering mengucek mata ketika mata terasa gatal. Ketika tangan Anda kotor sehabis memegang suatu benda dapat menginfeksi organ mata yang dapat berakibat sangat buruk untuk kesehatan mata.<br />
                                      <br />
                                      <li>Istirahatkan mata</li>
                                      Jangan paksakan mata untuk berlama-lama membaca buku atau berada di depan layar komputer. Jika Anda terlalu lama, mata akan sakit. Isitirahatkan mata selama beberapa menit.<br />
                                      <br />
                                      <li>Makan makanan yang mengandung vitamin A</li>
                                      Makan makanan seperti wortel yang mengandung <a href="https://bit.ly/2qBGykn" target="_blank">vitamin</a> A. Hal, ini dikarenakan wortel mengandung <a href="https://bit.ly/33beBNy" target="_blank">vitamin A</a> dan beta karoten yang berfungsi untuk menjaga kesehatan mata Anda.<br />
                                      <br />
                                      <li>Pakai pelindung mata</li>
                                      Cobalah untuk memakai pelindung mata dalam berbagai kegiatan aktivitas seperti saat berkendara, olahraga, dan sebagainya. Banyak kacamata yang dirancang khusus untuk olahraga untuk menghindari terjadinya cedera mata dan terhindar dari debu.<br />
                                      <br />
                                      <li>Periksa mata</li>
                                      Periksakan mata Anda secara rutin agar dapat terhindar dari penyakit yang menyerang mata sewaktu-waktu.<br />
                                      <br />
                                      <li>Berhenti merokok</li>
                                      Bagi orang perokok, usahakan agar menghentikan kebiasaan merokok mulai sekarang. Ini dikarenakan, dapat berpotensi mengalami sejumlah penyakit mata seperti katarak dan kerusakan saraf optik pada mata.<br />
                                      <br />
                                    </ol>
                                    Demikianlah tips cara menjaga kesehatan mata. Jagalah kesehatan mata Anda mulai sekarang. Baca juga artikel mengenai <a href="https://bit.ly/33aa51Y" target="_blank">fungsi protein</a>. Semoga bermanfaat dan terima kasih. - Sumber: http://ayokesehatan.blogspot.com/2014/01/8-tips-cara-menjaga-kesehatan-mata.html#sthash.gWBVXpXA.dpuf
